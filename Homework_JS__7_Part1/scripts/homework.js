// Changing the text of all paragraphs
let paragraphs = document.querySelectorAll('p');
for (let i = 0; i < paragraphs.length; i++) {
  paragraphs[i].innerText = 'The new text for the paragraph.';
}

// Changing the text of all h1 headers
let h1Headers = document.querySelectorAll('h1');
for (let i = 0; i < h1Headers.length; i++) {
  h1Headers[i].innerText = 'The new text for the h1 header.';
}

// Changing the text of all h3 headers
let h3Headers = document.querySelectorAll('h3');
for (let i = 0; i < h3Headers.length; i++) {
  h3Headers[i].innerText = 'The new text for the h3 header.';
}





